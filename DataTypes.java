public class DataTypes {

   static byte myByte = 127;
   static int myInt = 3;
    static float myFloat = 3.14f;
    static short myShort = 300;
   static long myLong = 314243456743L;
    static double myDouble = 3.12234;
    static char myChar = 'A';
    static String myString = "Saaiil";
    static boolean myBool = true;
    
    public static void main(String[] args) {

        DataTypes d1 = new DataTypes();

        System.out.println(d1.myByte);
        System.out.println(d1.myInt);
        System.out.println(d1.myFloat);
        System.out.println(d1.myShort);
        System.out.println(d1.myLong);
        System.out.println(d1.myDouble);
        System.out.println(d1.myChar);
        System.out.println(d1.myString);
        System.out.println(d1.myBool);

    }
}
